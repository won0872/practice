package kr.co.wmhr.base.to;

public class MenuTO {
	String menuCode, superMenuCode, menuName, menuUrl;

	public String getMenuCode() {
		return menuCode;
	}

	public void setMenuCode(String menuCode) {
		this.menuCode = menuCode;
	}

	public String getSuperMenuCode() {
		return superMenuCode;
	}

	public void setSuperMenuCode(String superMenuCode) {
		this.superMenuCode = superMenuCode;
	}

	public String getMenuName() {
		return menuName;
	}

	public void setMenuName(String menuName) {
		this.menuName = menuName;
	}

	public String getMenuUrl() {
		return menuUrl;
	}

	public void setMenuUrl(String menuUrl) {
		this.menuUrl = menuUrl;
	}

	
}
